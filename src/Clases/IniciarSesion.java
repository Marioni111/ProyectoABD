    private void btnInicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInicioActionPerformed
       
        Conexiones.Conexion.setCuenta(txtUsuario.getText(), txtPassword.getText());
        Conexiones.Conexion.getConnection();
        if(Conexiones.Conexion.getStatus()){
            JuegosDisponibles obj = new JuegosDisponibles();
            obj.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "USUARIO Y PASSWORD INCORRECTOS","Error De Conexion",JOptionPane.ERROR_MESSAGE);
            txtUsuario.setText("");
            txtPassword.setText("");
        }
    }//GEN-LAST:event_btnInicioActionPerformed

